package Lessons.Lesson8;

public class ifTest {
    public static void main(String[] args) {
        int num = 3;
        if (num > 3) {
            System.out.println("число больше 3");
        } else if (num == 3) {
            System.out.println("число равно 3");
        } else {
            System.out.println("число меньше 3");
        }
    }

}
